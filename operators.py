# Operators
##############################################################################
# Arithmetic operator
##############################################################################
a = int(input())
b = int(input())
print(a**b) #exponentiation
print(a+b) #addition
print(a-b) #subtraction
print(a/b) #division
print(a%b) #module / reminder
print(a*b) #multiplication
print(a//b) #floor division
print("_________________________________________")

##############################################################################
# Comparison Operators
##############################################################################
print(a < b) # if a less than b it is true otherwise false
print(a > b) # if a greater than b it is true otherwise false
print(a <= b) # if a less than or equal to b it is true otherwise false
print(a >= b) # if a greater than or equal to b it is true otherwise false
print(a == b) # if a equal to b it is true otherwise false
print(a != b) # if a not equal to b it is true otherwise false
print("_________________________________________")

