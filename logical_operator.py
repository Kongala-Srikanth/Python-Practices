# AND Operator
print("AND Operator")
print(True and True)
print(True and False)
print(False and True)
print(False and False)
print("_________________________________________")

# OR Operator
print("OR Operator")
print(True or True)
print(True or False)
print(False or True)
print(False or False)
print("_________________________________________")

# NOT Operator
print("NOT Operator")
print(not True)
print(not False)